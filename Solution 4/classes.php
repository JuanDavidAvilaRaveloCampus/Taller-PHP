<?php

    class Person {

        public $name;
        public $age;





        public function get_Name(){
            return $this->name;
        }

        public function set_Name($name_){
            $this->name = $name_;
        }



        public function get_Age(){
            return $this->age;
        }

        public function set_Age($age_){
            $this->age = $age_;
        }

    }





?>


